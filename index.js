function addToCart(title) {
  const selectedItem = categories.flatMap(category => category.items).find(item => item.title === title);

  if (selectedItem) {
    const existingCartItem = cart.find(item => item.id === selectedItem.id);

    if (existingCartItem) {
      existingCartItem.quantity++;
      existingCartItem.totalPrice = existingCartItem.quantity * existingCartItem.price;
    } else {
      const newCartItem = {
        ...selectedItem,
        quantity: 1,
        totalPrice: selectedItem.price
      };
      cart.push(newCartItem);

    }

    displayCart();
    updateCartStorage();
  }

}

function removeFromCart(index) {
  cart.splice(index, 1);
  displayCart();
  updateCartStorage();
}

function displayCart() {
  const cartItemContainer = document.getElementById('cart-item');
  const countElement = document.getElementById('count');
  const totalElement = document.getElementById('total');
  let total = 0;
  let count = 0;

  cartItemContainer.innerHTML = '';

  if (cart.length === 0) {
    cartItemContainer.textContent = 'Your cart is empty';
    countElement.textContent = '0';
    totalElement.textContent = 'Ksh 0.00';
  } else {
    cart.forEach((item, index) => {
      const { image, title, price, quantity, totalPrice } = item;
      total += totalPrice;
      count += quantity;

      const cartItem = document.createElement('li');
      cartItem.classList.add('flex', 'py-6');

      cartItem.innerHTML = `
      <div class="d-flex border-bottom p-2">

        <div class="w-25 h-100 p-1 flex-shrink-0 overflow-hidden rounded-md border border-gray-200">
          <img src="${image}" alt="Product" class="img-fluid h-auto">
        </div>

        <div class="d-block">
            <a class="fw-bold cart-text">${title}</a>
              <p class="cart-text d-flex"> Quantity: <input id="quantity-${index}" min="1" name="quantity" value="${quantity}" type="number" class="form-btn" onchange="updateQuantity(${index}, this.value)">
              </p>
            <p class="unit-price cart-text">Ksh ${price}.00</p>
        </div>

        <div class="">
            <button type="button" onclick="removeFromCart(${index})" class="font-medium fw-bold text-danger">X</button>
        </div>

      </div>
      `;

      cartItemContainer.appendChild(cartItem);
    });

    countElement.textContent = count.toString();
    totalElement.textContent = `Ksh ${total.toFixed(2)}`;

    const unitPriceElements = document.querySelectorAll('.unit-price');
    unitPriceElements.forEach((element, index) => {
      const cartItem = cart[index];
      element.textContent = `Ksh ${cartItem.price * cartItem.quantity}.00`;
    });
  }
}

function updateQuantity(index, newQuantity) {
  const cartItem = cart[index];
  cartItem.quantity = parseInt(newQuantity);
  cartItem.totalPrice = cartItem.quantity * cartItem.price;
  displayCart();
  updateCartStorage();
}

function updateCartStorage() {
  localStorage.setItem('cart', JSON.stringify(cart));
  localStorage.setItem('cartCount', cart.length);
}

function loadCartFromStorage() {
  const savedCart = localStorage.getItem('cart');
  const savedCartCount = localStorage.getItem('cartCount');

  if (savedCart) {
    cart = JSON.parse(savedCart);
  }

  if (savedCartCount) {
    const countElement = document.getElementById('count');
    countElement.textContent = savedCartCount;
  }
}

